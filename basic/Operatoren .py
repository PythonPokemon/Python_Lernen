# Operatoren – Überblick & Anwendung

# Arithmetische Operatoren
a = 10
b = 3
print(a + b)   # Addition: 13
print(a - b)   # Subtraktion: 7
print(a * b)   # Multiplikation: 30
print(a / b)   # Division: 3.333...
print(a // b)  # Ganzzahldivision: 3
print(a % b)   # Modulo: 1
print(a ** b)  # Potenz: 1000

# Vergleichsoperatoren
print(a > b)   # True
print(a == b)  # False

# Logische Operatoren
x = True
y = False
print(x and y)  # False
print(x or y)   # True
print(not x)    # False
