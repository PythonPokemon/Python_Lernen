""" 
Beispiel: .sort() - Liste in-place sortieren (verändert die Original-Liste)

 .sort() sortiert nur an Listen-Objekten..
"""

def liste_sortieren():
    zahlen = [5, 3, 9, 1]
    zahlen.sort()  # <---verändert die Original-Liste in aufsteigender reihenfolge bsp.123456789
    print("Sortierte Liste:", zahlen)

liste_sortieren()   # Methodenaufruf
