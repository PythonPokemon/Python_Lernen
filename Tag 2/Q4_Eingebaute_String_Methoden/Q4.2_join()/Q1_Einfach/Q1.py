"""
🟢 Einfache Aufgabe
Wörter zu einem Satz verbinden

🧩 Lernziel: Verstehen, wie .join() eine Liste von Strings zu einem String zusammensetzt.
"""

woerter = ["Ich", "lerne", "Python",]
satz = "-".join(woerter)           # Verbindet die Wörter mit einem Bindestrich
print(satz)                        # Ausgabe: Ich-lerne-Python