# 'if', 'else', 'def' sind Beispiele für Schlüsselwörter.
# Einrückung ist in Python zwingend erforderlich, um Blöcke zu markieren.

alter = 18

if alter >= 18:
    print("Du bist volljährig.\n")  # Diese Zeile ist eingerückt -> gehört zum if-Block
else:
    print("Du bist noch nicht volljährig.\n")  # Ebenfalls eingerückt